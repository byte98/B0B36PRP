// colors.h
// File which contains definitions of ASCII codes to colour
// console output.

#ifndef _COLORS_H
#define _COLORS_H

/* Definitions of some ASCII colours */
#define RED "\033[0;31m"	//Red colour
#define BRED "\033[1;31m"	//Red colour BOLD
#define GREEN "\033[0;32m"	//Green colour
#define BGREEN "\033[1;32m"	//Green colour BOLD
#define YELLOW "\033[0;33m"	//Yellow colour
#define BYELLOW "\033[01;33m"	//Yellow colour BOLD
#define BLUE "\033[0;34m"	//Blue colour
#define BBLUE "\033[1;34m"	//Blue colour BOLD
#define MAGENTA "\033[0;35m"	//Magenta colour
#define BMAGENTA "\033[1;35m"	//Magenta colour BOLD
#define CYAN "\033[0;36m"	//Cyan colour
#define BCYAN "\033[1;36m"	//Cyan colour BOLD
#define RST "\033[0m"		//Reset to system default

#endif //HW09_COLORS_H
